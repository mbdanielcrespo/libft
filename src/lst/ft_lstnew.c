#include <libft.h>

t_list	*ft_lstnew(int *content)
{
	t_list	*ls;

	ls = (t_list *)malloc(sizeof(t_list));
	if (!ls)
		return (NULL);
	ls->content = content;
	ls->next = NULL;
	return (ls);
}
